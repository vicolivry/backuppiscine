int ft_sqrt(int nb)
{
	int	i;
	int	result;

	i = 1;
	while (i <= nb / i)
	{
		if (i * i == nb)
		{	
			if (nb % i == 0)
				result = nb / i;
			else
				result = 0;
		}
		i++;
	}
	return (result);
}
